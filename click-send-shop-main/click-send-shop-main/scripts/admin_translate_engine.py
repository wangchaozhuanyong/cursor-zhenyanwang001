def translate_admin_zh(zh: str) -> str:
    zh = zh.strip()
    if not zh:
        return zh
    if zh in PHRASES:
        return PHRASES[zh]

    pat = apply_pattern_rules(zh)
    if pat:
        return pat

    m = re.match(r"^(.+)(中…|中\.\.\.|\.\.\.)$", zh)
    if m:
        base = m.group(1)
        if base in ING_SUFFIX:
            return f"{ING_SUFFIX[base]}..."
        if base in WORDS:
            return f"{WORDS[base]}..."

    if zh.startswith("已"):
        rest = zh[1:]
        if rest in PAST_PREFIX:
            return PAST_PREFIX[rest]
        if rest in WORDS:
            w = WORDS[rest]
            return w if w.endswith(("ed", "en", "t")) else f"{w}d" if w.endswith("e") else f"{w}ed"

    if zh.startswith("未"):
        rest = zh[1:]
        if rest in NOT_PREFIX:
            return NOT_PREFIX[rest]
        if rest in WORDS:
            return f"Not {WORDS[rest].lower()}"

    if zh.startswith("暂无"):
        rest = zh[2:]
        if rest in NONE_PREFIX:
            return NONE_PREFIX[rest]
        if rest in WORDS:
            return f"No {WORDS[rest].lower()}"

    if zh in WORDS:
        return WORDS[zh]

    # Compound: try joining known words (longest first)
    if len(zh) >= 2 and _has_cjk(zh):
        parts: list[str] = []
        i = 0
        while i < len(zh):
            matched = False
            for length in range(min(6, len(zh) - i), 0, -1):
                chunk = zh[i : i + length]
                if chunk in WORDS:
                    parts.append(WORDS[chunk])
                    i += length
                    matched = True
                    break
            if not matched:
                return zh
        if parts:
            return " ".join(parts)

    return zh


def polish_translation(zh: str, en: str) -> str:
    """Fix leftover CJK or broken hybrid strings."""
    if not _has_cjk(en) and " in progress" not in en and "Not " not in en[-6:]:
        return en
    better = translate_admin_zh(zh)
    return better if better != zh else en
