def _sub(zh: str) -> str:
    """Translate inner fragment (no pattern recursion)."""
    if zh in PHRASES:
        return PHRASES[zh]
    if zh in WORDS:
        return WORDS[zh]
    if len(zh) >= 2:
        parts: list[str] = []
        i = 0
        while i < len(zh):
            matched = False
            for length in range(min(8, len(zh) - i), 0, -1):
                chunk = zh[i : i + length]
                if chunk in WORDS:
                    parts.append(WORDS[chunk])
                    i += length
                    matched = True
                    break
            if not matched:
                return zh
        if parts:
            return _join(parts)
    return zh


